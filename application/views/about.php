				RMI dilakukan oleh Penilai Independen, maka Direktur yang membidangi
				pengelolaan risiko menunjuk pihak independen eksternal untuk melakukan penilaian
				dengan mendapatkan persetujuan dari Dewan Komisaris.
				b. Tim yang akan dibentuk dalam pelaksanaan Penilaian Internal memiliki pengetahuan
