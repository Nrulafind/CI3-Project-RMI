				Gambar II. 1 Tingkatan Hasil Penilaian RMI
				Skoring pada Penilaian RMI menggunakan skala 1-5 untuk kemudian tingkatan hasil
				Penilaian RMI terdiri atas,
				1. Fase Awal (Initial Phase), terdiri dari 2 (dua) sub-tingkat:
				a. Initial Phase A: BUMN belum menerapkan atau telah menerapkan sejumlah
				Dimensi praktik Manajemen Risiko. Budaya Risiko dan kesadaran akan Risiko para
				pegawai masih rendah. Tata kelola dan struktur organisasi yang berkaitan dengan
				Manajemen Risiko belum dibentuk dan masih dalam tahap perencanaan. Kerangka
				kerja, kepatuhan, proses, sistem dan alat yang berkaitan dengan Manajemen
				Risiko belum dimiliki, atau belum diterapkan secara konsisten di seluruh organisasi.
				Skor Penilaian RMI berkisar antara 1,0 hingga 1,4 dari skala 5.
				b. Initial Phase AA: Praktik Manajemen Risiko melampaui “Initial Phase”, dan sedang
				dalam transisi menuju “Emerging State”. Skor Penilaian RMI berkisar antara 1,5
				hingga 1,8 dari skala 5.
				2. Fase Berkembang (Emerging State), terdiri dari 2 (dua) sub-tingkat:
				a. Emerging State A: BUMN secara rata-rata menerapkan seluruh Dimensi praktik
				Manajemen Risiko untuk memenuhi persyaratan peraturan minimum. Pegawai
				tertentu cukup sadar akan Risiko, terutama di jajaran manajemen atas. Terdapat
				struktur organisasi, kerangka kerja, kepatuhan, proses, sistem, dan alat yang
				28
				berkaitan dengan Manajemen Risiko, namun masih ada kesenjangan dalam
				praktiknya, serta efektivitas atau level implementasinya masih bervariasi di setiap
				Dimensi. Skor Penilaian RMI berkisar antara 1,9 hingga 2,4 dari skala 5.
				b. Emerging State AA: Praktik Manajemen Risiko melampaui “Emerging State”, dan
				sedang dalam transisi menuju “Good Practice”. Skor Penilaian RMI berkisar antara
				2,5 hingga 2,8 dari skala 5.
				3. Fase Praktik Yang Baik (Good Practice Phase), terdiri dari 2 (dua) sub-tingkat:
				a. Good Practice A: BUMN menerapkan seluruh Dimensi praktik Manajemen Risiko
				yang rata-rata mendekati atau sejalan dengan praktik standar industrinya. Ratarata pegawai cukup sadar akan Risiko. Struktur organisasi, kerangka kerja,
