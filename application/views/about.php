				kerja, kepatuhan, proses, sistem, dan alat yang berkaitan dengan Manajemen Risiko
				diterapkan secara efektif di semua Dimensi, dan terus ditingkatkan. Skor Penilaian RMI
				BUMN berkisar antara 4,9 hingga 5,0 dari skala 5.
