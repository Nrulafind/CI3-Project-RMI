				sedang dalam transisi menuju “Strong Practice”. Skor Penilaian RMI berkisar
				antara 3,5 hingga 3,8 dari skala 5.
				4. Fase Praktik Yang Lebih Baik (Strong Practice Phase), terdiri dari 2 (dua) subtingkat:
				a. Strong Practice A: BUMN menerapkan seluruh Dimensi praktik Manajemen Risiko
				yang kuat, yang secara rata-rata mendekati atau sejalan dengan praktik standar
				global industrinya. Semua pegawai cukup sadar akan Risiko. Struktur organisasi,
				kerangka kerja, kepatuhan, proses, sistem, dan alat yang berkaitan dengan
				Manajemen Risiko diimplementasikan cukup efektif di sebagian besar Dimensi.
